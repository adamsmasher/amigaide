 SECTION BcdCode,CODE

ToBCD:
	lea.l OnesTable,a0
	move.b (a0,d0),d1	; d1 = ones digit
	lea.l TensTable,a0
	move.b (a0,d0),d0	; d0 = tens
	or.b d1,d0
	rts
	
 XDEF ToBCD

